package com.example.fsl.classes;

public class Statistic {
    private int id;
    private String name;
    private int teamId;

    public Statistic(){

    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTeamId() {
        return this.teamId;
    }
}
